<a name="7.1"></a><h2>7.1 Creating a Test or Survey</h2>
<p>To begin creating a test/survey, select the <em>Create Test/Survey</em> link. By filling out the information on the Create Test/Survey page you will have specified all the administrative options for a test, but actual questions are added to the test in a seperate step.</p>

<a name="7.2"></a><h2>7.2 Adding Questions to a Test</h2>
<p>To manage questions contained in a test, you must select the test you want from the Test & Surveys table and then select the <code>Questions</code> button. Assuming you have questions in the <em>Question Database</em> you can add them to the test through this interface. Each question in your test has a weight which needs to be specified.</p>

<a name="7.3"></a><h2>7.3 The Question Database</h2>
<p>The <em>Question Database</em> is where questions for tests and surveys are stored for your course. Questions are created seperately from tests which makes them reusable between tests and capable of being randomized when administered.</p>

<a name="7.4"></a><h2>7.4 Question Categories</h2>
<p>Questions can be grouped into categories which make it easier to add questions into tests.</p>

