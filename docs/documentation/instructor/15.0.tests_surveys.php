	<h1>7. Tests & Surveys</h1>
	<p>ATutor has the functionality to create tests and surveys to be administered to students enrolled in your course. There are many different options for delivering a test such as randomized questions, and group-specific tests.</p>

	<p>Tests & Surveys can be found under the Manage tab when logged in as a course's instructor.</p>

