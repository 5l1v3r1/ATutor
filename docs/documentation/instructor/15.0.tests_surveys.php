<h2>15. Tests and Surveys</h2>
	<p>Instructor and privileged students can create tests and surveys to be administered to students enrolled in a course. There are many different options for delivering tests such as randomized questions, or group-specific tests.</p>

	<p>A test is created by defining the properties using <em>Create Test/Survey</em>, then selecting questions from the Question Database to add to the test.</p>

