<h2>2. Backups</h2>
	<p>ATutor offers the facility to create backups of your course and restore the contents of those backups at any time. This is very useful for duplicating a course or saving the entire course for safe keeping in the case of accidental loss.</p>

	<p>The Backups utility is found under the <em>Manage</em> section and is only available to Instructors.</p>

	<p>Each course can store only a finite number of backups. That limit can be altered by the Administrator using the System Preferences option.</p>

