<h2>14. Student Tools</h2>
	<p>Student tools is a collection of functions that may be useful to students. As an instructor, you can determine the location where these tools can be accessed: <em>Main Navigation</em>, or <em>Home</em>.</p>

	<dl>
	<dt>Main Navigation</dt>
	<dd>Main Navigation is the area appearing below the title of the course on the interface and appear as "tabs". Student tools that are selected to appear here will have their own tab.</dd>

	<dt>Home</dt>
	<dd>Home is the "front" page seen when a student selects your course from the list of available courses. Student tools that are selected to appear in the Home page will be displayed with an icon and a label.</dd>
	</dl>

	<p>If neither <em>Main Navigation</em> or <em>Home</em> options are selected, then that Student Tool will not be accessible by students.</p>

	<p>Selecting the arrows in the <em>Order</em> column will change the ordering of the student tool as it appears in the <em>Main Navigation</em> and in the <em>Home</em> page.</p>
