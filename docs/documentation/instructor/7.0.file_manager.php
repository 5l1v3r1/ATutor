	<h1>6. File Manager</h1>
	<p>ATutor has a filesystem used for storing your course content and is managed by the <em>File Manager</em>. The File Manager is one of the main ways of getting content into your course since it allows you to upload content from your local system or from resources around the Internet. The File Manager also allows you to edit, create, and delete files.</p>

	<p>The File Manager can be found under the <em>Manage</em> tab.</p>

