	<a name="6.1"></a><h2>6.1 The File Manager Interface</h2>
	<p>The following are the main components of the File Manager interface.</p>
	<dl>
		<dt>Create Folder</dt>
		<dd><code>Create Folder</code> creates a folder on the ATutor system so you can organize your uploaded files. You can create folders and move files into folders at any time you like.</dd>

		<dt>Browse...</dt>
		<dd><code>Browse...</code> opens a local file browser window so you can select the file you want to upload.</dd>

		<dt>Upload</dt>
		<dd><code>Upload</code> will upload the specified file to the ATutor system. You can specify a file by either typing the path and filename in the <kbd>text field</kbd> or by using the <code>Browse...</code> button.</dd>

		<dt>Create a New File</dt>
		<dd>The <em>Create a New File</em> link will display a new interface where you can quickly create a new text or HTML file. If using Text mode, any blank lines will be saved with the file. If using HTML, you should be familiar with using HTML tags as no visual editor is provided. Selecting <code>Save</code> will save a new file with your specified information (filename and content) into the ATutor system and bring you back to the File Manager. <code>Cancel</code> will discard the file and bring you back to the File Manager.</dd>

		<dt>Rename</dt>
		<dd><code>Rename</code> button renames a single, selected file.</dd>

		<dt>Delete and Moving Files</dt>
		<dd><code>Delete</code> and <code>Move</code> buttons deletes or moves the selected files and folders (and its contents) from the ATutor system. You can select multiple files and folders for deletion or moving.</dd>
	</dl>
