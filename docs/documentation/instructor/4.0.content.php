<h2>4. Content</h2>
	<p>Content in ATutor can be managed in many ways and can be imported and created from many different sources, such as manually by entering content directly, or created from HTML files, standards compliant content packages, or from a learning objects content repository. Content that already exists in ATutor can also be exported into any of the mentioned formats. Only instructors and students who are given content privilages can manage course content.</p>

