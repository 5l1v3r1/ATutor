<h2>11. Polls</h2>
	<p>Polls is a <a href="./14.0.student_tools.php">Student Tool</a> that allows instructors and privleged students post a question with up to seven selectable answers. Unlike <a href="./15.0.tests_surveys.php">Tests and Surveys</a>, Polls are not graded. Because Polls is a <a href="./14.0.student_tools.php">Student Tool</a>, it can be enabled, disabled, and positioned on the interface according to the Student Tools preferences.</p>

