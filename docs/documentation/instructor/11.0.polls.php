<h2>11. Polls</h2>
	<p>Polls is a <a href="./14.0.student_tools.php">Student Tool</a> that allows instructors and privileged students to post a question with up to seven choices. Unlike <a href="./15.0.tests_surveys.php">Tests and Surveys</a>, Polls are not graded. Because Polls is a <a href="./14.0.student_tools.php">Student Tool</a>, it can be enabled, disabled, and positioned  according to the Student Tools preferences. Polls are useful for quickly gathering opinions.</p>

