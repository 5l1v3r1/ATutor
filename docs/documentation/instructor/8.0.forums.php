<a name="3.1"></a><h2>3.1 Creating and Editing a Forum</h2>
<p>To create a new forum for a course, you must first be logged in as an instructor and have selected the course you want to modify. Going to the <em>Manage</em> tab, select the <em>Forums</em> option. The screen should now show you a list of all forums for the current course. If there are no forums for the course, this list will be empty.</p>

<p>To create a new forum, click on the <em>Create Forum</em> link along the top. If you want to edit an existing forum, select the forum you want from the list and click the <kbd>Edit</kbd> button.</p>

<p>Whether editing or creating a new forum, the interface is the same. There is a <em>Title</em> and <em>Description</em> field. Fill out the appropriate information and click <kbd>Save</kbd> or <kbd>Cancel</kbd>.

<a name="3.2"></a><h2>3.2 Managing a Forum</h2>
<p>Once a forum has been created, you should become familiar with the forum management tools. To access these tools you simply browse a forum while logged in as an instructor.</p>

<a name="3.2.1"></a><h3>3.2.1 Managing Threads</h3>
<p>For each thread in a forum, you can perform the following actions:</p>
<dl>
<dt>Sticky Thread</dt>
<dd><p>This keeps the specified thread at the top of the list. This is useful for keeping important information visible to forum users. Once a post has been stickied, it will have a "!" icon and remain at the top of the forum.</p>

<p>To unstick a thread, just click on the "Sticky Thread" icon again.</p>

<p>Possible uses of a sticky thread: course dates, forum rules, contact information, or important course material.</p></dd>

<dt>Lock Thread</dt>
<dd><p>When locking a thread you have two options: lock posting and reading, and lock posting only. The first option, lock posting and reading, closes the thread so that no one can read the contents or post replies. However, the title of the thread remains listed in the forum. The second option, lock posting only, allows users to read the entire thread but not post any replies to it. Locked threads will have a special "Lock" icon next to the Topic title.</p>

<p>You can change the lock option by clicking the <em>Unlock Thread</em> icon. By doing so you have the option of unlocking, close the thread to reading and posting, or close the thread to posting.</p></dd>

<dt>Delete Thread</dt>
<dd>You can delete a thread by clicking the <em>Delete Thread</em>. You will be asked to confirm the action by clicking <kbd>Yes</kbd> or <kbd>No</kbd>. Once a thread is deleted, it can not be undeleted.</dd>
</dl>

<a name="3.2.1"></a><h3>3.2.1 Managing Posts</h3>
<p>For each post in a given thread, the course instructor can perform the following administrative actions: Edit, and Delete.</p>

<dl>
<dt>Edit</dt>
<dd>Allows the instructor to edit the post title and the body of a post.</dd>

<dt>Delete</dt>
<dd>Allows the instructor to delete the post from the parent thread. Deleting the first post from a thread will delete the entire thread including all replies. A confirmation will be asked prior to each deletion.</dd>
</dl>

