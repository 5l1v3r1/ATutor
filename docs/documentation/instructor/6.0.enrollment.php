<a name="5.3"></a><h2>5.3 Un-enroll, Alumni</h2>
<p>Once a student has been enrolled, it is possible to remove them from being enrolled in the class. Alternatively, students on the un-enrolled list can be enrolled in the course by selecting the student(s) and then selecting <kbd>Enroll</kbd>.</p>

<p>Whether a student is on the <em>Enrolled</em> or <em>Un-enrolled</em> list, you can designate one or more students as an alumni of the course. This gives them access to all course content with the exception of the forums and tests/surveys.</p>

