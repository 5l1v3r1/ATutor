	<h1>5.0 Enrollment</h1>
	<p>The student list for a particular course determines which of your students have access to the course content. ATutor has the facility to allow instructors to create, import and export student lists. </p>
	
	<p>To administer the users in your course, you need to be logged in as the instructor and have selected the <em>Enrollment</em> option from the <em>Manage</em> tab.</p>

