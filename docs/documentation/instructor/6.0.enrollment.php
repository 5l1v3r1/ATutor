<a name="5.2"></a><h2>5.2 Groups, Roles & Privleges</h2>
<a name="5.2.1"></a><h3>5.2.1 Groups</h3>
<p>Students can be placed into groups which allows the instructor administer different tests to different groups.</p>

<a name="5.2.1"></a><h3>5.2.2 Roles & Privleges</h3>
<p>Each student in your course can be assigned a role and special administrative privleges. This allows some (or all) of your students to perform actions like manage the glossary, or administer the forums.</p>

<p>Roles act as labels for assigned students.</p>
