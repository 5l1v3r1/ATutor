<h2>15.2.1 Creating Test Questions</h2>
	<p>Test questions are created through the <a href="./15.2.question_database.php">Question Database</a>. You can create four different types of questions: multiple choice, true or false, open ended, and likert. You will be presented with different options depending on the type of question you want to create. All new questions, when saved, will be added to the Question Database where it can be used in Tests or Surveys.</p>

