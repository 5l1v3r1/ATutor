<h2>Photo Gallery</h2>

<p>A photo gallery designed with accessibility in mind, allowing members to share course related photos and personal photos.</p>

<h3>Basic Photo Album Features</h3>

<dl>
<dt>Photo Gallery</dt>
<dd>This is the place that keep all your personal albums.  Both Private Album and Shared Album will be listed here.</dd>

<dt>Profile Album</dt>
<dd>This is an album where you keep your profile pictures.  In the future, this will be linked to your profile page. </dd>

<dt>Course Albums</dt>
<dd>This is the place that keep all the course albums.  Anyone enrolled in the course is allowed to upload photos; students are allowed to edit/delete the photos they have uploaded.  Instructors have the privilege to edit/delete any photos within his/her course albums. </dd>

<dt>Shared Albums</dt>
<dd>This will show all albums with the "Shared" permission in the ATutor installation.  All users have the permission to browse and comment, but are not allowed to upload or edit photos.  </dd>

<dt>Create Album</dt>
<dd>To create an album.  You will have to specify the album name and its permission; location and description are optional.  If you are an instructor, you will have an extra option of "Album Type", which allows you to create "My Album" or "Course Album".  Under "Album Permission", "Private" means this album is not accessible by any other users except yourself, "Shared" means this album will be displayed under the "Shared Albums" tab, and will be accessible by ALL users.</dd>
</dl>


