<dl>
<dt>Profile Album</dt>
<dd>This is an album where you keep your profile pictures. </dd>

<dt>My Albums</dt>
<dd>This is the place that keep all your personal albums.  Album listed here can be shared with anyone by giving them the URL.s</dd>

<dt>Course Albums</dt>
<dd>This is the place that keep all the course albums.  Only the course instructor and the assistant with the photo album privilege can add photos.  Any student that is enrolled in the course will be able to see the associated albums via this page.</dd>

