<h2>4.3 Course Categories</h2>
		<p>Categories are used for grouping related courses while browsing courses. Courses can be associated with categories when they are created, or at a later time by editing their properties. Course categories can also be associated with Themes if the <a href="2.2.system_preferences.php">System Preferences</a> <em>Theme Specific Categories</em> option is enabled.</p>

